    height = size * 3. / 2
    def scale(p):
        x,y = size*((p[0]-6)/18.)+size/2, height + size*((-(p[1]+7))/18.)-1 
        if round:
            return round(x), round(y)
        else:
            return x, y
    for c in s:
        if c != '_':
            try:
                v = hp1345_font_data[ord(c)]
            except:
                v = []
        else:
            v = [ [(-18,-6),(36,0)], [(0,0)] ]
        if len(v):
            pos = (x,y)
            for stroke in v[:-1]:
                
                newPos = None
                
                if dotSize > 0 and len(stroke) == 2 and stroke[1] == (0,0):
                    returnToOriginal = pos
